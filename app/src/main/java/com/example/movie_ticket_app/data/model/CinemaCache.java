package com.example.movie_ticket_app.data.model;

public class CinemaCache {
    int cinemaId;
    String cinemaName;
    String cinemaStreet;
    String cinemaProvince;
    String cinemaDistrict;
    String cinemaWard;

    public int getCinemaId() {
        return cinemaId;
    }

    public String getCinemaName() {
        return cinemaName;
    }

    public String getCinemaStreet() {
        return cinemaStreet;
    }

    public String getCinemaDistrict() {
        return cinemaDistrict;
    }

    public String getCinemaProvince() {
        return cinemaProvince;
    }

    public String getCinemaWard() {
        return cinemaWard;
    }
}
